class Dualzeri{
	private double zero;
	private int iteration;
	private int success;
	
	public Dualzeri(){
		;
	}
	public double getZero(){
		return zero;
	}
	public void setZero(double d){
		this.zero=d;
	}
	public int getIteration(){
		return iteration;
	}
	public void setIteration(int i){
		iteration=i;
	}
	public int getSuccess(){
		return success;
	}
	public void setSuccess(int i){
		success=i;
	}
}